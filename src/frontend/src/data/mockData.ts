export function formatPrice(priceCents: bigint): string {
  return `₹${(Number(priceCents) / 100).toFixed(2)}`;
}

export function getElapsedMinutes(createdAt: bigint): number {
  const nowMs = Date.now();
  const createdMs = Number(createdAt) / 1_000_000;
  return Math.floor((nowMs - createdMs) / 60000);
}
